package com.example.calculator;



public class Calculator {

    public static int add(int a, int b){
        int result = a + b;
        return result;
    }

    public int subtraction(int a, int b){
        int result = a - b;
        return result;
    }

    public int multiplication(int a, int b){
        int result = a * b;
        return result;
    }

    public int division(int a, int b){
        int result = a / b;
        return result;
    }
}
